// Initialize localStorage with demo data if empty
export function initLocalData() {
  const categories = localStorage.getItem('category')
  const items = localStorage.getItem('item')

  if (!categories) {
    localStorage.setItem('category', JSON.stringify(demoCategories))
  }

  if (!items) {
    localStorage.setItem('item', JSON.stringify(demoItems))
  }
}
